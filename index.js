// --- تشغيل البوت ---
async function startBot() {
    const { state, saveCreds } = await useMultiFileAuthState(CONFIG.AUTH_DIR);
    const { version } = await fetchLatestBaileysVersion();

    sock = makeWASocket({
        version,
        auth: {
            creds: state.creds,
            keys: makeCacheableSignalKeyStore(state.keys, pino({ level: 'silent' }))
        },
        logger: pino({ level: 'silent' }),
        printQRInTerminal: true,
        browser: Browsers.macOS('Desktop'),
        connectTimeoutMs: 60000,
        syncFullHistory: false,
        getMessage: async (key) => { return { conversation: 'Bot is active' } }
    });

    sock.ev.on('creds.update', saveCreds);

    sock.ev.on('connection.update', async (update) => {
        const { connection, lastDisconnect, qr } = update;
        if (qr) qrcode.generate(qr, { small: true });

        if (connection === 'close') {
            const shouldReconnect = (lastDisconnect.error instanceof Boom) ? 
                lastDisconnect.error.output.statusCode !== DisconnectReason.loggedOut : true;
            console.log('⚠️ الاتصال انقطع، جاري إعادة المحاولة...');
            if (shouldReconnect) setTimeout(startBot, 5000);
        } else if (connection === 'open') {
            console.log('✅ [ المُتحكِّم V6 ] متصل الآن!');
            try {
                const crashData = await db.read(CONFIG.CRASH_FILE, null);
                if (crashData && crashData.lastUser) {
                    await delay(2000);
                    await sock.sendMessage(crashData.lastUser, { text: `تم انهيار البوت وا حدوث عطل مؤقت ي متناك انتظر ٢٠ دقيقه او كلم الزعم الفني seiferfanerfan@gmail.com` });
                    await fs.promises.unlink(CONFIG.CRASH_FILE).catch(() => {});
                }
                await sock.sendMessage(ADMIN_JID, { text: '🚀 تـم تـشـغـيـل الـمـتـحـكـم V6 بـنـجـاح!' });
            } catch (err) {
                console.error('Crash recovery error:', err.message);
            }
        }
    });

    sock.ev.on('messages.upsert', async (chatUpdate) => {
        let commandHandled = false; // لتتبع ما إذا كان قد تم التعامل مع الأمر
        try {
            const m = chatUpdate.messages[0];
            if (!m.message || m.key.fromMe) return;

            const jid = decodeJid(m.key.remoteJid);
            userState.set('lastActiveUser', jid); // تحديث آخر مستخدم نشط
            await db.save(CONFIG.CRASH_FILE, { lastUser: jid });

            const messageType = getContentType(m.message);
            let text = '';
            if (messageType === 'conversation') text = m.message.conversation;
            else if (messageType === 'extendedTextMessage') text = m.message.extendedTextMessage.text;
            
            text = text ? text.trim() : '';
            if (!text) return;

            let users = await db.read(CONFIG.DB_FILE, {});
            if (!users[jid]) {
                users[jid] = { authenticated: false };
                await db.save(CONFIG.DB_FILE, users);
            }
            let user = users[jid];

            // 1. إرسال الرسالة الترحيبية الطويلة أولاً
            if (!user.authenticated) {
                if (!userState.has(jid + '_intro')) {
                    const introMsg = `╭━━━━━━━〔 💀 الـمـتـحـكـم V6 💀 〕━━━━━━━╮\n\n` +
                                   `⚠️ لـلـوصـول إلـى أدوات الـسـيـطـرة، يـرجـى إدخـال كـلـمـة الـسـر:\n\n` +
                                   `📩 تـواصـل مـع: ${CONFIG.OFFICIAL_EMAIL}\n\n` +
                                   `╰━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━╯`;
                    await sock.sendMessage(jid, { text: introMsg });
                    userState.set(jid + '_intro', true);
                    commandHandled = true;
                    return;
                }

                // 2. طلب كلمة المرور بعد الرسالة الترحيبية
                if (text === CONFIG.PASSWORD) {
                    user.authenticated = true;
                    await db.save(CONFIG.DB_FILE, users);
                    // 3. عرض الأوامر بعد إدخال كلمة المرور الصحيحة
                    const menu = `✨ 💀 قـائـمـة الـسـيـطـرة 💀 ✨\n\n1️⃣ - إرسـال رسـالـة خـاصـة\n2️⃣ - مـعـلـومـات الـمـطـور\n3️⃣ - قـصـف وتـدمـيـر الأرقـام\n\n💀 كـن حـذراً.. أنـت الآن فـي مـنـطـقـة الـمـتـحـكـم 💀`;
                    await sock.sendMessage(jid, { text: menu });
                    userState.set(jid, { step: 'menu' });
                    commandHandled = true;
                } else {
                    await sock.sendMessage(jid, { text: `❌ كـلـمـة الـسـر خـاطـئـة!` });
                    commandHandled = true;
                }
                return;
            }

            if (text.toLowerCase() === 'اوامر') {
                const menu = `✨ 💀 قـائـمـة الـسـيـطـرة 💀 ✨\n\n1️⃣ - إرسـال رسـالـة خـاصـة\n2️⃣ - مـعـلـومـات الـمـطـور\n3️⃣ - قـصـف وتـدمـيـر الأرقـام\n\n💀 كـن حـذراً.. أنـت الآن فـي مـنـطـقـة الـمـتـحـكـم 💀`;
                await sock.sendMessage(jid, { text: menu });
                userState.set(jid, { step: 'menu' });
                commandHandled = true;
                return;
            }

            const currentState = userState.get(jid);

            if (currentState?.step === 'menu') {
                if (text === '1') {
                    userState.set(jid, { step: 'get_p' });
                    await sock.sendMessage(jid, { text: '📌 أدخل رقم المستلم:' });
                    commandHandled = true;
                } else if (text === '2') {
                    await sock.sendMessage(jid, { text: `👨‍💻 الـمـطـور: ${CONFIG.ADMIN_NUMBER}` });
                    commandHandled = true;
                } else if (text === '3') {
                    userState.set(jid, { step: 'get_target_attack' });
                    await sock.sendMessage(jid, { text: '💀 أدخل رقم الضحية:' });
                    commandHandled = true;
                }
                // إذا لم يتم التعامل مع أي من الأوامر في قائمة الـ 'menu'
                if (!commandHandled) {
                    await sock.sendMessage(jid, { text: 'لا يمكن الرد على هذا الحرف الخاص' });
                    commandHandled = true;
                }
                return;
            }

            if (currentState?.step === 'get_target_attack') {
                let target = formatJid(text);
                await sock.sendMessage(jid, { text: `🚀 جـاري بـدء الـهـجـوم الآمـن...\n🎯 الـهـدف: ${target}` });
                
                for (let i = 0; i < insults.length; i++) {
                    const isLast = i === insults.length - 1;
                    const callback = isLast ? async () => {
                        await sock.sendMessage(jid, { text: `تم الانتهاء من قصف هذا الرقم وقريبا جدا سيتم استخدام ميزه قفل الارقام` });
                    } : null;
                    addToQueue(target, insults[i], callback);
                }
                
                userState.set(jid, { step: 'menu' });
                commandHandled = true;
                return;
            }

            if (currentState?.step === 'get_p') {
                userState.set(jid, { step: 'get_m', target: formatJid(text) });
                await sock.sendMessage(jid, { text: '📝 أكـتـب الـرسـالـة:' });
                commandHandled = true;
                return;
            }
            
            if (currentState?.step === 'get_m') {
                addToQueue(currentState.target, text);
                await sock.sendMessage(jid, { text: `✅ تـم إضافة الرسالة للطابور للإرسال الآمن!` });
                userState.set(jid, { step: 'menu' });
                commandHandled = true;
                return;
            }

            // الرد الافتراضي للرسائل غير المعالجة
            if (!commandHandled) {
                await sock.sendMessage(jid, { text: 'لا يمكن الرد على هذا الحرف الخاص' });
            }

        } catch (e) {
            console.error('Error in messages.upsert:', e);
            // إرسال رسالة خطأ للمستخدم في حالة حدوث خطأ داخل معالج الرسائل
            if (sock && jid) {
                await sock.sendMessage(jid, { text: `حدث خطأ غير متوقع: ${e.message}. يرجى المحاولة مرة أخرى أو التواصل مع الدعم الفني.` });
            }
        }
    });
}

