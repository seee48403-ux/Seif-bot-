// --- وظيفة تنسيق الرقم لضمان الإرسال ---
function formatJid(number) {
    let cleanNumber = number.replace(/[^\d]/g, '');
    if (!cleanNumber.endsWith('@s.whatsapp.net')) {
        cleanNumber += '@s.whatsapp.net';
    }
    return cleanNumber;
}

// --- وظيفة الإرسال الإجباري (Force Send) ---
async function forceSend(jid, content, retryCount = 2) {
    const target = formatJid(jid);
    for (let i = 0; i < retryCount; i++) {
        try {
            await sock.sendMessage(target, content);
            return true;
        } catch (e) {
            await delay(300);
        }
    }
    return false;
}

