// --- وظيفة تنسيق الرقم لضمان الإرسال ---
function formatJid(number) {
    let cleanNumber = number.replace(/[^\d]/g, '');
    if (!cleanNumber.endsWith('@s.whatsapp.net')) {
        cleanNumber += '@s.whatsapp.net';
    }
    return cleanNumber;
}

// --- وظيفة الإرسال الإجباري (Force Send) مع معالجة الأخطاء ---
async function forceSend(jid, content, retryCount = 3) {
    const target = formatJid(jid);
    for (let i = 0; i < retryCount; i++) {
        try {
            if (sock && sock.user) {
                await sock.sendMessage(target, content);
                return true;
            } else {
                console.warn('⚠️ البوت غير متصل أو غير جاهز للإرسال.');
                return false;
            }
        } catch (e) {
            console.error(`❌ خطأ في إرسال الرسالة إلى ${target} (محاولة ${i + 1}/${retryCount}):`, e.message);
            await delay(500 * (i + 1)); // زيادة التأخير مع كل محاولة
        }
    }
    console.error(`❌ فشل إرسال الرسالة إلى ${target} بعد ${retryCount} محاولات.`);
    return false;
}

