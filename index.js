// --- وظائف مساعدة ---
const decodeJid = (jid) => {
    if (!jid) return jid;
    if (/:\d+@/gi.test(jid)) {
        let decode = jidDecode(jid) || {};
        return decode.user && decode.server && decode.user + "@" + decode.server || jid;
    } else return jid;
};

const formatJid = (number) => {
    let clean = number.replace(/[^0-9]/g, "");
    // إزالة 00 إذا كانت موجودة في البداية
    if (clean.startsWith("00")) {
        clean = clean.substring(2);
    }
    // إضافة كود الدولة الافتراضي إذا بدأ الرقم بـ 0 وطوله 11 رقم (لأرقام الهواتف المصرية)
    if (clean.startsWith("0") && clean.length === 11) {
        clean = CONFIG.DEFAULT_COUNTRY_CODE + clean.substring(1);
    }
    // التأكد من أن الرقم لا يبدأ بـ '+' قبل إضافة '@s.whatsapp.net'
    if (clean.startsWith("+")) {
        clean = clean.substring(1);
    }
    return clean.endsWith("@s.whatsapp.net") ? clean : clean + "@s.whatsapp.net";
};

// --- نظام طابور الإرسال الجذري ---
const queue = [];
let isProcessing = false;

async function send(target, text) {
    queue.push({ target, text });
    if (!isProcessing) processQueue();
}

async function processQueue() {
    if (queue.length === 0) { isProcessing = false; return; }
    isProcessing = true;
    const { target, text } = queue.shift();
    try {
        if (sock && sock.authState.creds.registered) { // تأكد من أن البوت مسجل ومرتبط
            await sock.sendMessage(target, { text: text });
            console.log(`✅ تم الإرسال إلى ${target.split("@")[0]}`);
        } else {
            console.warn(`⚠️ البوت غير متصل أو غير مسجل. إعادة الرسالة إلى الطابور: ${text}`);
            queue.unshift({ target, text }); // إعادة الرسالة إلى الطابور للمحاولة لاحقاً
        }
    } catch (e) {
        console.error(`❌ فشل الإرسال إلى ${target.split("@")[0]}:`, e.message);
        // إعادة الرسالة إلى الطابور للمحاولة لاحقاً في حالة وجود خطأ
        queue.unshift({ target, text });
        await delay(5000); // تأخير قبل المحاولة مرة أخرى
    }
    await delay(CONFIG.ATTACK_DELAY);
    processQueue();
}

// --- تشغيل البوت ---
async function start() {
    const { state, saveCreds } = await useMultiFileAuthState(CONFIG.AUTH_DIR);
    const { version } = await fetchLatestBaileysVersion();

    sock = makeWASocket({
        version,
        auth: {
            creds: state.creds,
            keys: makeCacheableSignalKeyStore(state.keys, pino({ level: "silent" }))
        },
        logger: pino({ level: "silent" }),
        printQRInTerminal: true,
        browser: Browsers.macOS("Desktop"),
        // إضافة هذه الخيارات لضمان استقرار الاتصال
        syncFullHistory: false,
        connectTimeoutMs: 60000,
        defaultQueryTimeoutMs: undefined, // لتجنب مشاكل الـ timeout
        getMessage: async (key) => { return { conversation: "Bot is active" } }
    });

    sock.ev.on("creds.update", saveCreds);

    sock.ev.on("connection.update", async (u) => {
        const { connection, lastDisconnect, qr } = u;
        if (qr) qrcode.generate(qr, { small: true });

        if (connection === "close") {
            const shouldReconnect = (lastDisconnect.error instanceof Boom) ? 
                lastDisconnect.error.output.statusCode !== DisconnectReason.loggedOut : true;
            console.log("⚠️ الاتصال انقطع، جاري إعادة المحاولة...");
            if (shouldReconnect) {
                // إرسال رسالة الانهيار للمشرف قبل إعادة الاتصال
                if (sock && sock.authState.creds.registered) {
                    await sock.sendMessage(ADMIN_JID, { text: CONFIG.CRASH_MESSAGE }).catch(e => console.error("Failed to send crash message:", e.message));
                }
                start();
            } else {
                console.log("🚫 تم تسجيل الخروج بنجاح.");
            }
        } else if (connection === "open") {
            console.log("🚀 البوت شغال الآن بنسبة 200%!");
            // إرسال رسالة الاتصال الناجح للمشرف
            await send(ADMIN_JID, CONFIG.RECONNECT_MESSAGE);
        }
    });

    sock.ev.on("messages.upsert", async (m) => {
        const msg = m.messages[0];
        if (!msg.message || msg.key.fromMe) return;

        const jid = decodeJid(msg.key.remoteJid);
        const text = (msg.message.conversation || msg.message.extendedTextMessage?.text || "").trim();
        if (!text) return;

        let users = db.read();
        if (!users[jid]) users[jid] = { step: "intro" };
        let user = users[jid];

        // 1. الترحيب (أول رسالة)
        if (user.step === "intro") {
            const intro = `╭━━━━━━━━━━━━━━━━━━━━━╮\n        💀 المتحكم V6 💀\n╰━━━━━━━━━━━━━━━━━━━━━╯\n\nأهلاً بك في عالم السيطرة.\nيجب إدخال كلمة السر للمتابعة.`;
            await send(jid, intro);
            user.step = "password";
            db.save(users);
            return;
        }

        // 2. كلمة السر
        if (user.step === "password") {
            if (text === CONFIG.PASSWORD) {
                user.step = "menu";
                db.save(users);
                const menu = `✅ تم الدخول بنجاح!\n\n1️⃣ - إرسال رسالة\n2️⃣ - قصف بالمصفوفات\n\nاكتب الرقم المطلوب.`;
                await send(jid, menu);
            } else {
                await send(jid, "❌ كلمة السر خطأ!");
            }
            return;
        }

        // 3. الأوامر
        if (text.toLowerCase() === "اوامر") {
            user.step = "menu";
            db.save(users);
            await send(jid, `1️⃣ - إرسال رسالة\n2️⃣ - قصف بالمصفوفات`);
            return;
        }

        if (user.step === "menu") {
            if (text === "1") {
                user.step = "get_p";
                await send(jid, "📌 أدخل الرقم:");
            } else if (text === "2") {
                user.step = "attack";
                await send(jid, "💀 أدخل رقم الضحية للقصف:");
            } else {
                await send(jid, "🤔 أمر غير مفهوم. يرجى اختيار 1 أو 2 أو كتابة 'اوامر'.");
            }
            db.save(users);
            return;
        }

        if (user.step === "get_p") {
            user.target = formatJid(text);
            user.step = "get_m";
            db.save(users);
            await send(jid, "📝 اكتب الرسالة:");
            return;
        }

        if (user.step === "get_m") {
            if (user.target) {
                await send(user.target, text);
                await send(jid, `✅ تم إرسال الرسالة بنجاح إلى ${user.target.split("@")[0]}!`);
                user.step = "menu";
                user.target = null; // مسح الهدف بعد الإرسال
                db.save(users);
            } else {
                await send(jid, "❌ لم يتم تحديد مستلم. يرجى المحاولة مرة أخرى.");
                user.step = "menu";
                db.save(users);
            }
            return;
        }

        if (user.step === "attack") {
            const target = formatJid(text);
            await send(jid, `🚀 بدأ الهجوم على ${target.split("@")[0]}...`);
            for (const insult of insults) {
                await send(target, insult);
            }
            await send(jid, `✅ تم قصف الرقم بجميع المصفوفات!`);
            user.step = "menu";
            db.save(users);
            return;
        }
        
        // رسالة افتراضية إذا لم يتم التعامل مع أي أمر
        await send(jid, "🤔 أمر غير مفهوم. يرجى اختيار 1 أو 2 أو كتابة 'اوامر'.");
    });
}

start();
