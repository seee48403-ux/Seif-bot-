// --- وظيفة فك تشفير JID ---
const decodeJid = (jid) => {
    if (!jid) return jid;
    if (/:\d+@/gi.test(jid)) {
        let decode = jidDecode(jid) || {};
        return decode.user && decode.server && decode.user + '@' + decode.server || jid;
    } else return jid;
};

// --- تنسيق الرقم لـ JID ---
const formatJid = (number) => {
    let cleanNumber = number.replace(/[^\d]/g, '');
    if (!cleanNumber.endsWith('@s.whatsapp.net')) {
        cleanNumber += '@s.whatsapp.net';
    }
    return cleanNumber;
};

// --- نظام طابور الإرسال الآمن ---
const msgQueue = [];
let isProcessingQueue = false;

async function addToQueue(target, text, callback = null) {
    msgQueue.push({ target, text, callback });
    if (!isProcessingQueue) processQueue();
}

async function processQueue() {
    if (msgQueue.length === 0) {
        isProcessingQueue = false;
        return;
    }

    isProcessingQueue = true;
    const { target, text, callback } = msgQueue.shift();

    try {
        if (sock && sock.authState.creds) {
            await sock.sendPresenceUpdate('composing', target);
            await delay(800);
            await sock.sendMessage(target, { text });
            await sock.sendPresenceUpdate('paused', target);
            if (callback) await callback();
        }
    } catch (err) {
        console.error('Queue Error:', err.message);
        if (err.message.includes('Closed')) {
            msgQueue.unshift({ target, text, callback });
            await delay(5000);
        }
    }

    await delay(CONFIG.ATTACK_DELAY);
    processQueue();
}

