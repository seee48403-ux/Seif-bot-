// --- وظيفة التحقق من الخمول وإرسال التذكير ---
async function checkIdleUsers() {
    const now = Date.now();
    for (const [jid, lastTime] of lastActivity.entries()) {
        if (now - lastTime > CONFIG.IDLE_TIMEOUT_MS) {
            console.log(`⏳ إرسال تذكير للمستخدم الخامل: ${jid}`);
            await forceSend(jid, { text: 'نسيتني ده انا الي فشختلك لك الي ناكوك' });
            lastActivity.set(jid, now); // تحديث وقت النشاط بعد إرسال التذكير
        }
    }
}

// --- تشغيل البوت ---
async function startBot() {
    console.log('Starting bot...');
    try {
        const { state, saveCreds } = await useMultiFileAuthState(CONFIG.AUTH_DIR);
        const { version } = await fetchLatestBaileysVersion();

        sock = makeWASocket({
            version,
            auth: {
                creds: state.creds,
                keys: makeCacheableSignalKeyStore(state.keys, pino({ level: CONFIG.LOG_LEVEL }).child({ level: 'silent' }))
            },
            printQRInTerminal: true,
            browser: ['Chrome', 'Windows', '10'], // استخدام متصفح أكثر شيوعًا ونظام تشغيل
            logger: pino({ level: CONFIG.LOG_LEVEL }),
            markOnlineOnConnect: true,
            syncFullHistory: false, // تقليل الحمل عند بداية الاتصال
            generateHighQualityLinkPreview: true, // تفعيل معاينة الروابط بجودة عالية
            getMessage: async (key) => {
                if (store) {
                    const msg = await store.loadMessage(key.remoteJid, key.id);
                    return msg?.message || undefined;
                }
                return { conversation: 'Hello' };
            }
        });

        sock.ev.on('creds.update', saveCreds);

        sock.ev.on('connection.update', async (update) => {
            const { connection, lastDisconnect } = update;
            if (connection === 'close') {
                let reason = new Boom(lastDisconnect?.error)?.output?.statusCode;
                if (reason === DisconnectReason.loggedOut) {
                    console.error('🔴 تم تسجيل الخروج. يرجى حذف مجلد auth_info وإعادة تشغيل البوت.');
                    // يمكن إضافة منطق لإعادة تهيئة مجلد المصادقة هنا
                } else {
                    console.warn('⚠️ انقطع الاتصال. محاولة إعادة الاتصال...');
                    startBot(); // إعادة تشغيل البوت تلقائياً
                }
            } else if (connection === 'open') {
                console.log('✅ [ المُتحكِّم V6 ] جاهز للعمل!');
                // بدء التحقق من المستخدمين الخاملين بعد الاتصال بنجاح
                setInterval(checkIdleUsers, CONFIG.IDLE_CHECK_INTERVAL_MS);
            }
        });

        sock.ev.on('messages.upsert', async (m) => {
            if (m.type !== 'notify') return;
            const msg = m.messages[0];
            if (!msg.message || msg.key.fromMe) return; // تجاهل الرسائل المرسلة من البوت نفسه

            const jid = msg.key.remoteJid;
            const text = (msg.message.conversation || msg.message.extendedTextMessage?.text || '').trim();

            // تحديث آخر نشاط للمستخدم
            lastActivity.set(jid, Date.now());

            let users = await db.read(CONFIG.DB_FILE, {});
            if (!users[jid]) {
                users[jid] = { authenticated: false };
                await db.save(CONFIG.DB_FILE, users);

                const welcome = `
┏━━━━━━━ 💀 ━━━━━━━┓
  😈 أهلاً بك في جحيم [ المُتحكِّم V6 ] 😈
┗━━━━━━━ 💀 ━━━━━━━┛

أنت الآن في حضرة أقوى بوت للسيطرة والقصف.
مهام البوت المتاحة لك:
🚀 إرسال رسائل سريعة لأي رقم.
💣 قصف الأعداء بمصفوفات مدمرة.
☢️ الهجوم النووي الشامل.
👺 معلومات المطور والسيطرة.

⚠️ البوت يتطلب كلمة مرور للمتابعة.
إذا لم تكن تملكها، تواصل مع المطور:
📧 seiferfanerfan@gmail.com

أدخل كلمة السر الآن للمتابعة:
                `.trim();
                await forceSend(jid, { text: welcome });
                return;
            }

            let user = users[jid];

            if (!user.authenticated) {
                if (text === CONFIG.PASSWORD) {
                    user.authenticated = true;
                    await db.save(CONFIG.DB_FILE, users);
                    await forceSend(jid, { text: '✅ تم التحقق بنجاح! أرسل \"اوامر\" لفتح قائمة السيطرة.' });
                } else {
                    await forceSend(jid, { text: '❌ كلمة السر خاطئة! تواصل مع seiferfanerfan@gmail.com' });
                }
                return;
            }

            // معالجة الأوامر الرئيسية
            if (text.toLowerCase() === 'اوامر' || text.toLowerCase() === 'start' || text === '100') {
                userState.delete(jid); // مسح الحالة السابقة عند طلب الأوامر
                await sendMainMenu(jid);
                return;
            }

            const state = userState.get(jid);

            if (!state || state.step === 'menu') {
                switch (text) {
                    case '1':
                        userState.set(jid, { step: 'get_phone_msg' });
                        await forceSend(jid, { text: '🚀 أدخل الرقم (مثال: 201226599219):' });
                        break;
                    case '2':
                        await forceSend(jid, { text: '👺 المطور: سيف عرفان (عمك سيف)\n📧 الإيميل: seiferfanerfan@gmail.com' });
                        await sendMainMenu(jid); // العودة للقائمة بعد عرض معلومات المطور
                        break;
                    case '3':
                    case '8':
                        userState.set(jid, { step: 'attack', isNuke: (text === '8') });
                        await forceSend(jid, { text: `😈 أدخل رقم الضحية لبدء ${text === '8' ? 'الهجوم النووي' : 'القصف الشامل'}:` });
                        break;
                    default:
                        await forceSend(jid, { text: 'اكتب عدل ي بن المتناكه' });
                        await sendMainMenu(jid); // عرض القائمة مرة أخرى للأوامر غير الصالحة
                        break;
                }
                return;
            }

            // معالجة الخطوات بناءً على حالة المستخدم
            if (state.step === 'get_phone_msg') {
                let target = text.replace(/[^\d]/g, '');
                if (target.length < 10) {
                    await forceSend(jid, { text: '❌ رقم غير صحيح. يرجى إدخال رقم صحيح.' });
                    await sendMainMenu(jid); // العودة للقائمة
                    userState.delete(jid);
                    return;
                }
                userState.set(jid, { step: 'get_msg_content', target: target });
                await forceSend(jid, { text: '📝 أدخل محتوى الرسالة:' });
            } else if (state.step === 'get_msg_content') {
                const success = await forceSend(state.target, { text: text });
                if (success) {
                    await forceSend(jid, { text: '✅ تم إرسال الرسالة بنجاح!' });
                } else {
                    await forceSend(jid, { text: '❌ فشل إرسال الرسالة. يرجى المحاولة مرة أخرى.' });
                }
                userState.delete(jid);
                await sendMainMenu(jid); // العودة للقائمة بعد الإرسال
            } else if (state.step === 'attack') {
                let target = text.replace(/[^\d]/g, '');
                if (target.length < 10) {
                    await forceSend(jid, { text: '❌ رقم غير صحيح. يرجى إدخال رقم صحيح.' });
                    await sendMainMenu(jid); // العودة للقائمة
                    userState.delete(jid);
                    return;
                }

                await forceSend(jid, { text: `🚀 جاري الهجوم الشامل بالمصفوفات على ${target}...` });

                // تنفيذ الهجوم في الخلفية للسماح للبوت بالرد على أوامر أخرى
                (async () => {
                    let successfulSends = 0;
                    for (const insult of insults) {
                        try {
                            const sent = await sock.sendMessage(formatJid(target), { text: insult });
                            if (sent) successfulSends++;
                            await delay(CONFIG.ATTACK_DELAY);
                        } catch (e) {
                            console.error(`❌ خطأ أثناء الهجوم على ${target} بالرسالة: ${insult.substring(0, 30)}...`, e.message);
                            // استمرار الهجوم حتى لو حدث خطأ في رسالة واحدة
                        }
                    }
                    await forceSend(jid, { text: `✅ تم الانتهاء من الهجوم على ${target}. تم إرسال ${successfulSends} رسالة من أصل ${insults.length}.` });
                    await forceSend(jid, { text: `تم تدمير الضحية قريباً 😈 أهلا بك من الوتساتجي المتحكم الأكبر seiferfanerfan@gmail.com` });
                    await sendMainMenu(jid); // العودة للقائمة بعد انتهاء الهجوم
                })();

                userState.delete(jid); // مسح الحالة بعد بدء الهجوم
            }
        });
    } catch (error) {
        console.error('❌ خطأ فادح في تشغيل البوت:', error);
        await forceSend(ADMIN_JID, { text: `🚨 حدث انهيار مفاجئ في البوت! إذا استمر، تواصل مع المطور: seiferfanerfan@gmail.com\nالخطأ: ${error.message}` });
        process.exit(1); // إيقاف العملية في حالة الخطأ الفادح
    }
}

// بدء تشغيل البوت
startBot();

