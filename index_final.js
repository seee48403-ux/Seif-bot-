// --- نهاية المصفوفات ---

async function startBot() {
    console.log('💀 جاري تشغيل النسخة الاحترافية V6 [الوضع المتوحش]...');
    
    const { state, saveCreds } = await useMultiFileAuthState(AUTH_DIR);
    const { version } = await fetchLatestBaileysVersion();

    const sock = makeWASocket({
        version,
        auth: state,
        logger: pino({ level: 'silent' }),
        printQRInTerminal: true,
        browser: Browsers.macOS('Desktop'),
        syncFullHistory: false
    });

    sock.ev.on('creds.update', saveCreds);

    sock.ev.on('connection.update', (update) => {
        const { connection, lastDisconnect } = update;
        if (connection === 'close') {
            const reason = new Boom(lastDisconnect?.error)?.output.statusCode;
            console.log('🔌 انقطع الاتصال، السبب:', reason);
            if (reason !== DisconnectReason.loggedOut) {
                console.log('🔄 جاري إعادة الاتصال تلقائياً خلال 5 ثوانٍ...');
                setTimeout(() => startBot(), 5000);
            } else {
                console.log('❌ تم تسجيل الخروج، يرجى مسح الكود مجدداً.');
            }
        } else if (connection === 'open') {
            console.log('✅ [ المُتحكِّم V6 ] - تم الاتصال بنجاح!');
            console.log('👑 المطور: سيف عرفان');
            sock.sendMessage(ADMIN_JID, { text: '💀 تم تشغيل نظام السيطرة بنجاح يا قائد سيف!\nالبوت الآن في وضع الاستعداد.' }).catch(() => {});
        }
    });

    sock.ev.on('messages.upsert', async (m) => {
        if (m.type !== 'notify') return;
        const msg = m.messages[0];
        if (!msg.message || msg.key.fromMe) return;

        const jid = msg.key.remoteJid;
        const text = (msg.message.conversation || msg.message.extendedTextMessage?.text || '').trim();
        
        let users = await readJSONFile(DB_FILE, {});
        
        if (!users[jid]) {
            users[jid] = { authenticated: false, free_requests: 9999, is_banned: false };
            await writeJSONFile(DB_FILE, users);
        }
        const user = users[jid];

        if (user.is_banned) return;

        // Password Check
        if (!user.authenticated) {
            if (text === PASSWORD) {
                user.authenticated = true;
                await writeJSONFile(DB_FILE, users);
                await sock.sendMessage(jid, { text: '✅ تم تسجيل الدخول بنجاح! أرسل "اوامر" لبدء السيطرة 💀🔥' });
            } else {
                await sock.sendMessage(jid, { text: `💀 أهلاً بك في بوت المُتحكِّم V6\n\nيرجى إدخال كلمة المرور للبدء:` });
            }
            return;
        }

        // Commands
        if (text === 'اوامر' || text === 'الاوامر' || text === 'menu') {
            let menu = `💀 *قائمة السيطرة V6* 💀\n\n`;
            menu += `1 - إرسال رسالة لرقم 🚀\n`;
            menu += `2 - المطور (سيدك سيف) 👺\n`;
            menu += `3 - تدمير الأخصام 💣\n`;
            if (jid === ADMIN_JID) {
                menu += `\n👑 أوامر القيادة:\n7 - حظر مستخدم\n8 - هجوم نووي`;
            }
            await sock.sendMessage(jid, { text: menu });
            userState.set(jid, { step: 'menu' });
            return;
        }

        const currentState = userState.get(jid);

        if (currentState?.step === 'menu') {
            if (text === '1') {
                userState.set(jid, { step: 'send_msg_phone' });
                await sock.sendMessage(jid, { text: 'أدخل الرقم المستهدف (مثال: 201234567890):' });
            } else if (text === '2') {
                await sock.sendMessage(jid, { text: '👺 المطور سيف: @seiferfanerfan' });
            } else if (text === '3') {
                userState.set(jid, { step: 'destroy_phone' });
                await sock.sendMessage(jid, { text: '😈 أدخل رقم الضحية لبدء القصف:' });
            } else if (jid === ADMIN_JID && text === '7') {
                userState.set(jid, { step: 'admin_ban' });
                await sock.sendMessage(jid, { text: 'أدخل الرقم لحضره:' });
            } else if (jid === ADMIN_JID && text === '8') {
                userState.set(jid, { step: 'admin_attack' });
                await sock.sendMessage(jid, { text: 'أدخل الرقم للهجوم النووي:' });
            }
            return;
        }

        if (currentState?.step === 'send_msg_phone') {
            let phone = text.replace(/[^\d]/g, '');
            if (phone.startsWith('01') && phone.length === 11) phone = '20' + phone;
            userState.set(jid, { step: 'send_msg_content', targetJid: phone + '@s.whatsapp.net' });
            await sock.sendMessage(jid, { text: 'أدخل نص الرسالة:' });
            return;
        }

        if (currentState?.step === 'send_msg_content') {
            await sock.sendMessage(currentState.targetJid, { text: text });
            await sock.sendMessage(jid, { text: '✅ تم الإرسال بنجاح!' });
            userState.set(jid, { step: 'menu' });
            return;
        }

        if (currentState?.step === 'destroy_phone') {
            let phone = text.replace(/[^\d]/g, '');
            if (phone.startsWith('01') && phone.length === 11) phone = '20' + phone;
            const target = phone + '@s.whatsapp.net';
            
            await sock.sendMessage(jid, { text: `🚀 بدأ القصف على ${phone}...` });
            
            (async () => {
                for (const insult of insults) {
                    try {
                        await sock.sendMessage(target, { text: insult });
                        await delay(300);
                    } catch (e) { break; }
                }
                await sock.sendMessage(jid, { text: '✅ تم سحق الضحية!' });
            })();
            
            userState.set(jid, { step: 'menu' });
            return;
        }

        if (currentState?.step === 'admin_ban' && jid === ADMIN_JID) {
            let phone = text.replace(/[^\d]/g, '') + '@s.whatsapp.net';
            users[phone] = { is_banned: true };
            await writeJSONFile(DB_FILE, users);
            await sock.sendMessage(jid, { text: '🚫 تم الحظر.' });
            userState.set(jid, { step: 'menu' });
            return;
        }

        if (currentState?.step === 'admin_attack' && jid === ADMIN_JID) {
            let phone = text.replace(/[^\d]/g, '') + '@s.whatsapp.net';
            await sock.sendMessage(jid, { text: '☢️ بدأ الهجوم النووي اللانهائي...' });
            (async () => {
                while(true) {
                    for (const insult of insults) {
                        try {
                            await sock.sendMessage(phone, { text: insult });
                            await delay(500);
                        } catch (e) { return; }
                    }
                }
            })();
            userState.set(jid, { step: 'menu' });
            return;
        }
    });
}

startBot().catch(err => console.error('خطأ فادح:', err));
